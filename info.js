function foiTemplate(state) {

	var monthNames = ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"];

	n = new Date();
	y = n.getFullYear();
	m = monthNames[n.getMonth()]
	d = n.getDate();
	dateString = m + " " + d + ", " + y;

	instructionsString = state.email
		? "<div class='form-instructions'>This state uses an email address to direct prisoner Freedom of Information Requests."
			+ "<br>Please fill out the following form to generate a copyable email.</div>"
			+ "<div class='email-instructions'><b>Email your letter to: " + state.email + "</div></b>"
		: "<div class='form-instructions'>Please fill out the following form to generate a printable document.</div>";

	addressString = state.email ? "" : (
				"<div>"
				 + "<p>"
					 + "<div> <textarea placeholder='Your Name'></textarea> </div>" 
					 + "<div> <textarea placeholder='Street Address'></textarea> </div>" 
					 + "<div> <textarea placeholder='City, State, Zipcode'></textarea> </div>" 
				 + "</p>"
				+ "</div>"
				 ) 

	printOrCopyButton = state.email 
		? "<button class='copybutton' onclick='copyThis(\"" + state.key + "\")'>Copy to Clipboard (In Arial Font)</button>"
			+ "<button class='uncopybutton' onclick='restoreTemplate()'>Back</button>"
		: "<button class='printbutton' onclick='printThis(\"" + state.key + "\")'>Print</button>"
		
	
	htmlString = 
				 instructionsString + 
				 "<div class='form-disclaimer'>FreeIAm does not collect information about our users.</div>"

				 	 + "<div class='body-text'>"

					 + addressString

					 + "<p>"
						 + "<div>" + dateString + "</div>"
					 + "</p>"
					
					 + "<p>"	
						 + "<div>" + state.address1 + "</div>" 
						 + "<div>" + state.address2 + "</div>" 
						 + "<div>" + state.address3 + "</div>" 
						 + "<div>" + state.address4 + "</div>" 
					 + "</p>"

					 + "<p>" + "To Whom It May Concern:" + "</p>"

					 + "<p>" + "Under the "
						+ state.law + ", " 
						+ "I am requesting an opportunity to inspect or obtain copies of public records that "
						+ "<span>"
							+ "<textarea style='width: 725px; height: 50px;' placeholder='Describe the records or information sought with enough detail for the public agency to respond.  Be as specific as your knowledge of the available records will allow. But it is more important to describe the information you are seeking.'></textarea>" 
						+ "</span>"
						+ "If there are any fees for searching or copying these records, please inform me if the cost will exceed <textarea style='width: 55px; height: 15px;' placeholder='$______'></textarea>. "
						+ "<div></div>"
						+ "However, I would also like to request a waiver of all fees in that the disclosure of the requested information is in the public interest and will contribute significantly to the public’s understanding of "
						+ "<span>"
							+ "<textarea style='width: 725px; height: 33px' placeholder='Fill in what this will contribute to'></textarea>. "
						+ "</span>"
						+ "<div style='margin-bottom: 10px'>"
							+ "<textarea style='width: 725px; height: 33px' placeholder='Here, you can identify yourself as a representative of the news media if applicable and state that your request is related to news gathering purposes. '></textarea>"
						+ "</div>"
						+ "This information is not being sought for commercial purposes. "

						+ "<p>"
						  + state.otherText
						+ "</p>"
					 + "</p>"

					 + "<p>"
					  + "Sincerely, "
					  + "<div>"
					 	 + "<textarea placeholder='Your Name'></textarea>"
					  + "</div>"
					  + "<div>"
					 	 + "<textarea placeholder='Your Phone Number'></textarea>"
					  + "</div>"
					 + "</p>"
				 + "</div>"
				 + printOrCopyButton
	     + "</div>"

	return htmlString;
}

function printThis(state) {
	var prePrintHTML = $('#' + state + ' ~ .state-info-p').clone();

	$('#' + state + ' ~ .state-info-p textarea').each(function() {
		$(this).replaceWith($(this).val());
	});
	$('.printable').css({"margin-top": "50px"});
	$('.state-header').hide();
	$('.form-instructions, .form-disclaimer').hide();
	$('.printbutton').hide();

	window.print(); // triggers @media print styles

	$('#' + state + ' ~ .state-info-p').replaceWith(prePrintHTML);
	$('.printable').css({"margin-top": "0"});
	$('.state-header').show();
	$('.form-instructions, .form-disclaimer').show();
	$('.printbutton').show();
}

function copyThis(state) {
	var prePrintHTML = $('#' + state + ' ~ .state-info-p').clone();

	globals.template = {
		state: state,
		html: prePrintHTML
	};

	$('#' + state + ' ~ .state-info-p textarea').each(function() {
		$(this).replaceWith($(this).val());
	});

	$('.printable').css({"margin-top": "50px"});
	$('.body-text').css({"font-family": "Arial"});
	$('.state-header').hide();
	$('.form-instructions, .form-disclaimer').hide();
	$('.copybutton').hide();

	$('.email-instructions').show();
	$('.uncopybutton').show();
}

function restoreTemplate() {
	$('#' + globals.template.state + ' ~ .state-info-p').html(globals.template.html);
	$('.uncopybutton').hide();
	$('.email-instructions').hide();
}

$(document).ready(function() {
	for (state in foiInfo) {
		if (foiInfo[state].form) {
			htmlString = "<div>This state uses an online form to file Freedom of Information requests. "
			+ "Use <a href='" + foiInfo[state].form + "'>this</a> site to file the request."
			+ "</div>"
			$('#' + state + " ~ .state-info-p").html(htmlString);
		} else {
			$('#' + state + " ~ .state-info-p").html(foiTemplate(foiInfo[state]));
		}
	}
	$('.email-instructions').hide();
	$('.uncopybutton').hide();
})